    private void btnSrcActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSrcActionPerformed
       String search=JOptionPane.showInputDialog(null,"Enter the Employee ID number of the employee to search","Search");
         DBConnect con1=new DBConnect();
          con1.connect();
        
        try {
            Statement s1=con1.connect().createStatement();
            ResultSet rs1=s1.executeQuery("select * from staff where eno='"+search+"'");
            while(rs1.next()){
                
                txtSid.setText(rs1.getString(1));
                txtName.setText(rs1.getString(2));
                cmbCentre.setSelectedItem(rs1.getString(3));
                dateDOB.setDate(rs1.getDate(4));
                txtPhone.setText(rs1.getString(6));
                txtSemail.setText(rs1.getString(7));
                txtSnic.setText(rs1.getString(8));
                if(rs1.getString(5).equals("Male")){
                    rdbMale.setSelected(true);
                    
                }
                else{
                    rdbFemale.setSelected(true);
                    
                }
            
            
            }
            }
        catch(SQLException e){
            System.out.println(e.getMessage());
        }
         if(txtSid.getText().isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Entry Not Found", "Error", ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSrcActionPerformed

    private void btnDelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDelActionPerformed
        String eno=JOptionPane.showInputDialog(rootPane, "Enter the Staff ID to delete data","Delete Employee Data",WARNING_MESSAGE);
        
        DBConnect con1=new DBConnect();
      
          con1.connect();
        try {
            Statement s1=con1.connect().createStatement();
            int co=s1.executeUpdate("delete from staff where eno='"+eno+"'");
            if(co==1){
                JOptionPane.showMessageDialog(rootPane, "Deleted");
            }
            else{
                JOptionPane.showMessageDialog(rootPane, "Could not delete.Try Again after checking the Staff ID Number","Error",ERROR_MESSAGE);
            }
        }
        catch(SQLException e){
            System.out.print(e.getMessage());
        }
    }//GEN-LAST:event_btnDelActionPerformed

