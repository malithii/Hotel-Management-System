    private void btnRegActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegActionPerformed
    String eno,ename,ecentre,email,nic,gender,dob,phone;
    eno=txtSid.getText();
    ename=txtName.getText();
    ecentre=cmbCentre.getSelectedItem().toString();
    phone=txtPhone.getText();
    email=txtSemail.getText();
    
    nic=txtSnic.getText();
    
    dob=((JTextField)dateDOB.getDateEditor().getUiComponent()).getText();
    
    
    
    if(rdbMale.isSelected()){
        gender="Male";
       
    }
    else{
        gender="Female";
    }
    Pattern p=Pattern.compile("^[0-9]{10}$");
          Matcher mat=p.matcher(phone);
          Pattern p2=Pattern.compile("^[a-zA-Z0-9]+[@]{1}+[a-zA-Z0-9]+[.]{1}+[com]+$");
          Matcher mat2=p2.matcher(email);
          Pattern p3 =Pattern.compile("^[a-zA-Z]+$");
          Matcher mat3=p3.matcher(ename);
    if(eno.isEmpty()||ename.isEmpty()||ecentre.isEmpty()||phone.isEmpty()||email.isEmpty()||nic.isEmpty()||gender.isEmpty()||dob.isEmpty()){
                    JOptionPane.showMessageDialog(rootPane, "Please Fill out All the details","Empty Fields",WARNING_MESSAGE);
    }
     else if(!mat.matches()){
            JOptionPane.showMessageDialog(rootPane, "You have entered an invalid telephone number","Invalid entry",ERROR_MESSAGE);
       }
           else if(!mat2.matches()){
               JOptionPane.showMessageDialog(rootPane, "You have entered an invalid e-mail","Invalid entry",ERROR_MESSAGE);
           }
             else if(!mat3.matches()){
               JOptionPane.showMessageDialog(rootPane, "Please donot use any symbols or numbers in your name","Invalid entry",ERROR_MESSAGE);
           }
    else{
    DBConnect con=new DBConnect();
    Connection c1=con.connect();
    try{
        Statement s1=c1.createStatement();
        int co=s1.executeUpdate("insert into staff values('"+eno+"','"+ename+"','"+ecentre+"','"+dob+"','"+gender+"','"+phone+"','"+email+"','"+nic+"')");
        if(co==1){
            JOptionPane.showMessageDialog(null, "Successful Entry");
        }
    }
    catch(SQLException e){
        System.out.print(e.getMessage());
    }
    
    
    txtSid.setText(null);
    txtName.setText(null);
    txtPhone.setText(null);
    txtSemail.setText(null);
    txtSnic.setText(null);
    buttonGroup1.clearSelection();
    cmbCentre.setSelectedIndex(0);
    dateDOB.setDate(null);
            }
    }//GEN-LAST:event_btnRegActionPerformed

