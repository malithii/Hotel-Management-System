    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
String stoid=JOptionPane.showInputDialog(rootPane, "Enter the Staff ID to delete data","Delete Employee Data",WARNING_MESSAGE);
        
        DBConnect con1=new DBConnect();
      
          con1.connect();
        try {
            Statement s1=con1.connect().createStatement();
            int co=s1.executeUpdate("delete from store where stoid='"+stoid+"'");
            if(co==1){
                JOptionPane.showMessageDialog(rootPane, "Deleted");
            }
            else{
                JOptionPane.showMessageDialog(rootPane, "Could not delete.Try Again after checking the Stock ID Number","Error",ERROR_MESSAGE);
            }
        }
        catch(SQLException e){
            System.out.print(e.getMessage());
        }       
    }//GEN-LAST:event_btnRemoveActionPerformed

    private void btnSrchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSrchActionPerformed
      String search=JOptionPane.showInputDialog(null,"Enter the stock ID number of the goods to search","Search");
         DBConnect con1=new DBConnect();
          con1.connect();
        
        try {
            Statement s1=con1.connect().createStatement();
            ResultSet rs1=s1.executeQuery("select * from store where stoid='"+search+"'");
            while(rs1.next()){
                
                txtStockId.setText(rs1.getString(1));
                txtStckname.setText(rs1.getString(2));
                cmbStckCenter.setSelectedItem(rs1.getString(3));
                txtExp.setDate(rs1.getDate(6));
                txtStckvalue.setText(rs1.getString(4));
                txtDesc.setText(rs1.getString(5));
               
                
            
            
            }
            }
        catch(SQLException e){
            System.out.println(e.getMessage());
        }
         if(txtStockId.getText().isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Entry Not Found", "Error", ERROR_MESSAGE);
        }
                                       
    }//GEN-LAST:event_btnSrchActionPerformed

