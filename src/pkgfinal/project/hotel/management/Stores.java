    private void btnRegActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegActionPerformed
        String stid,stname,stcenter,stdesc,stexp;float stvalue;
        stid=txtStockId.getText();
        stname=txtStckname.getText();
        stcenter=cmbStckCenter.getSelectedItem().toString();
        stdesc=txtDesc.getText();
        stexp=((JTextField)txtExp.getDateEditor().getUiComponent()).getText();
        stvalue=Float.parseFloat(txtStckvalue.getText());
        boolean chg=false;
        if(stvalue==0f){
            chg=true;
            
        }
        if(stid.isEmpty()||stname.isEmpty()||stcenter.isEmpty()||stdesc.isEmpty()||stexp.isEmpty()||chg){
            JOptionPane.showMessageDialog(rootPane, "Please Fill out All the details","Empty Fields",WARNING_MESSAGE);
        }
        
        else{ DBConnect con=new DBConnect();
        Connection c=con.connect();
        try{
            Statement s1=c.createStatement();
            int x=s1.executeUpdate("insert into store values('"+stid+"','"+stname+"','"+stcenter+"','"+stvalue+"','"+stdesc+"','"+stexp+"')");
            if(x==1){
                JOptionPane.showMessageDialog(null, "Successful Entry");
            }
        }
        catch(SQLException e){
            System.out.print(e.getMessage());
        }
        
        txtStockId.setText(null);
        txtStckname.setText(null);
        cmbStckCenter.setSelectedIndex(0);
        txtDesc.setText(null);
        txtExp.setDate(null);
        txtStckvalue.setText(null);
        }
    }//GEN-LAST:event_btnRegActionPerformed

