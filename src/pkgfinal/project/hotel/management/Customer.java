    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
             String Cno,cname,cmail,caddress,country = null,id,pay,phone;
          Cno=txtCno.getText();
          cname=txtNname.getText();
          cmail=txtCmail.getText();
          caddress=txtAdrs.getText();
          try{
              country=(String) localeCountry.getSelectedItem();
          }
          catch(ArrayIndexOutOfBoundsException e)
          {
              e.getMessage();
          }
          phone=txtCphone.getText();
          id=txtNICPP.getText();
          if(chkFull.isSelected()){
              pay="Fully Paid";
          }
          else if(chkPart.isSelected()){
              pay="Partial";
          }
          else
          {
              pay="Not Paid";
          }
          Pattern p=Pattern.compile("^[0-9]{10}$");
          Matcher mat=p.matcher(phone);
          Pattern p2=Pattern.compile("^[a-zA-Z0-9]+[@]{1}+[a-zA-Z0-9]+[.]{1}+[com]+$");
          Matcher mat2=p2.matcher(cmail);
          Pattern p3 =Pattern.compile("^[a-zA-Z]+$");
          Matcher mat3=p3.matcher(cname);
          
           if(cname.isEmpty()||Cno.isEmpty()||cmail.isEmpty()||caddress.isEmpty()||country.isEmpty()||id.isEmpty()||pay.isEmpty()||phone.isEmpty()){
               JOptionPane.showMessageDialog(rootPane, "Please Fillout every detail");
           
       }
           else if(!mat.matches()){
            JOptionPane.showMessageDialog(rootPane, "You have entered an invalid telephone number","Invalid entry",ERROR_MESSAGE);
       }
           else if(!mat2.matches()){
               JOptionPane.showMessageDialog(rootPane, "You have entered an invalid e-mail","Invalid entry",ERROR_MESSAGE);
           }
             else if(!mat3.matches()){
               JOptionPane.showMessageDialog(rootPane, "Please donot use any symbols or numbers in your name","Invalid entry",ERROR_MESSAGE);
           }
        else{ 
          DBConnect con1=new DBConnect();
          con1.connect();
        try {
            Statement s1=con1.connect().createStatement();
            int co=s1.executeUpdate("insert into customer values('"+Cno+"','"+cname+"','"+phone+"','"+cmail+"','"+caddress+"','"+country+"','"+id+"','"+pay+"')");
            if(co==1){
                JOptionPane.showMessageDialog(null,"Successful Entry");
            }
        } catch (SQLException ex) {
          ex.getMessage();
         
        }
        txtCno.setText(null);
        txtCno.setText(null);
          txtNname.setText(null);
          txtCmail.setText(null);
          txtAdrs.setText(null);
          localeCountry.setSelectedIndex(0);
          txtCphone.setText(null);
          txtNICPP.setText(null);
          buttonGroup1.clearSelection();
                }
    }//GEN-LAST:event_btnAddActionPerformed

