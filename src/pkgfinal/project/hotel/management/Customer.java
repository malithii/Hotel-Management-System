    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
       String search=JOptionPane.showInputDialog(null,"Enter the Staff ID number of the employee to search","Search");
         DBConnect con1=new DBConnect();
          con1.connect();
        try {
            Statement s1=con1.connect().createStatement();
            ResultSet rs1=s1.executeQuery("select * from customer where cno='"+search+"'");
            while(rs1.next()){
                txtCno.setText(rs1.getString(1));
                txtNname.setText(rs1.getString(2));
                txtCphone.setText(rs1.getString(3));
                txtCmail.setText(rs1.getString(4));
                txtAdrs.setText(rs1.getString(5));
                localeCountry.setSelectedItem(rs1.getString(6));
                txtNICPP.setText(rs1.getString(7));
                if(chkFull.getActionCommand().equals(rs1.getString(8))){
                    chkFull.setSelected(true);
                
                    
                }
                else if(chkNot.getActionCommand().equals(rs1.getString(8))){
                            chkNot.setSelected(true);
                            }
                else{
                    chkPart.setSelected(true);
                }
            }
        }
        catch(SQLException e){
            System.out.print(e.getMessage());
        }
        if(txtCno.getText().isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Entry Not Found", "Error", ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSearchActionPerformed

    private void btnDelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDelActionPerformed
      String cno=JOptionPane.showInputDialog(rootPane, "Enter the Customer Number to delete data","Delete CustomerData",WARNING_MESSAGE);
        
        DBConnect con1=new DBConnect();
      
          con1.connect();
        try {
            Statement s1=con1.connect().createStatement();
            int co=s1.executeUpdate("delete from customer where cno='"+cno+"'");
            if(co==1){
                JOptionPane.showMessageDialog(rootPane, "Deleted");
            }
            else{
                JOptionPane.showMessageDialog(rootPane, "Could not delete.Try Again after checking the Customer Number","Error",ERROR_MESSAGE);
            }
        }
        catch(SQLException e){
            System.out.print(e.getMessage());
        }
    }//GEN-LAST:event_btnDelActionPerformed

    private void txtNnameKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNnameKeyPressed
       char c=evt.getKeyChar();
       if(Character.isLetter(c)||Character.isWhitespace(c)||Character.isISOControl(c)){
           txtNname.setEditable(true);
       }
       else{
           txtNname.setEditable(false);
       }
    }//GEN-LAST:event_txtNnameKeyPressed

